package com.foxconn.tcutils.constant;

public enum BUEnum {
	
	DT("Desktop", "DT"), MNT("Monitor", "MNT"), PRT("Printer", "PRT"), DBA("dba", "DBA");
	private final String buName;
    private final String buCode;
    
    private BUEnum(String buName, String buCode) {
    	this.buName = buName;
    	this.buCode = buCode;
    }
	
    public String buName() {
        return this.buName;
    }

    public String buCode() {
        return this.buCode;
    }
    
}
