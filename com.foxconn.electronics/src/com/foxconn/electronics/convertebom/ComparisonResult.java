package com.foxconn.electronics.convertebom;

import java.util.ArrayList;
import java.util.List;

public class ComparisonResult {

	List<BOMLinePojo> addBOMLine;
	List<BOMLinePojo> deleteBOMLine;
	List<BOMLinePojo> modifyBOMLine;
	
	public List<BOMLinePojo> getAddBOMLine() {
		return addBOMLine;
	}
	public void setAddBOMLine(List<BOMLinePojo> addBOMLine) {
		this.addBOMLine = addBOMLine;
	}
	public List<BOMLinePojo> getDeleteBOMLine() {
		return deleteBOMLine;
	}
	public void setDeleteBOMLine(List<BOMLinePojo> deleteBOMLine) {
		this.deleteBOMLine = deleteBOMLine;
	}
	public List<BOMLinePojo> getModifyBOMLine() {
		return modifyBOMLine;
	}
	public void setModifyBOMLine(List<BOMLinePojo> modifyBOMLine) {
		this.modifyBOMLine = modifyBOMLine;
	}
	
	@Override
	public String toString() {
		return "ComparisonResult [addBOMLine=" + addBOMLine + ", deleteBOMLine=" + deleteBOMLine + ", modifyBOMLine="
				+ modifyBOMLine + "]";
	}
	
}
