package com.foxconn.electronics.convertebom;

import java.util.List;

import com.teamcenter.rac.kernel.TCComponentItemRevision;

public class EBOMStructurePojo {

	private BOMLinePojo parent;
	private List<EBOMStructurePojo> children;
	private List<TCComponentItemRevision> substitute;
	
	public BOMLinePojo getParent() {
		return parent;
	}
	public void setParent(BOMLinePojo parent) {
		this.parent = parent;
	}
	public List<EBOMStructurePojo> getChildren() {
		return children;
	}
	public void setChildren(List<EBOMStructurePojo> children) {
		this.children = children;
	}
	public List<TCComponentItemRevision> getSubstitute() {
		return substitute;
	}
	public void setSubstitute(List<TCComponentItemRevision> substitute) {
		this.substitute = substitute;
	}
	
	@Override
	public String toString() {
		return "EBOMStructurePojo [parent=" + parent + ", children=" + children + ", substitute=" + substitute + "]";
	}
	
}
