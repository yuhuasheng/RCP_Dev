package com.foxconn.decompile.pojo;

public class BomPojo {
	private String parentNumber=" ";
	private String itemNumber=" ";
	private String findNum=" ";
	private String descr=" ";
	private String mfg=" ";
	private String mfgPN=" ";
    private String version=" ";
    private String action=" ";
    
    
    
	public String getParentNumber() {
		return parentNumber;
	}
	public void setParentNumber(String parentNumber) {
		this.parentNumber = parentNumber;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public String getItemNumber() {
		return itemNumber;
	}
	public void setItemNumber(String itemNumber) {
		this.itemNumber = itemNumber;
	}
	public String getFindNum() {
		return findNum;
	}
	public void setFindNum(String findNum) {
		this.findNum = findNum;
	}
	public String getDescr() {
		return descr;
	}
	public void setDescr(String descr) {
		this.descr = descr;
	}
	public String getMfg() {
		return mfg;
	}
	public void setMfg(String mfg) {
		this.mfg = mfg;
	}
	public String getMfgPN() {
		return mfgPN;
	}
	public void setMfgPN(String mfgPN) {
		this.mfgPN = mfgPN;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
    
    
}
