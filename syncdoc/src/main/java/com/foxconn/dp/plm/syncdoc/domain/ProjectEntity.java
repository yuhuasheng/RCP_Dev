package com.foxconn.dp.plm.syncdoc.domain;

import java.util.Date;

public class ProjectEntity {
    private Long proSn;
    private String projName;
    private String projSpasId;
    private Long folderId;
    private Date created;

    public Long getProSn() {
        return proSn;
    }

    public void setProSn(Long proSn) {
        this.proSn = proSn;
    }

    public String getProjName() {
        return projName;
    }

    public void setProjName(String projName) {
        this.projName = projName;
    }

    public String getProjSpasId() {
        return projSpasId;
    }

    public void setProjSpasId(String projSpasId) {
        this.projSpasId = projSpasId;
    }

    public Long getFolderId() {
        return folderId;
    }

    public void setFolderId(Long folderId) {
        this.folderId = folderId;
    }

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }
}
