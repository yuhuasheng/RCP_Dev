package com.foxconn.dp.plm.syncdoc.domain;

public class ProjectInfo {

	private String tcSpasId;
	private String tcProjectName;
	private String dmsSpasId;
	private String dmsProjectName;
	private String dmsFolderId;
	
	public String getTcSpasId() {
		return tcSpasId;
	}
	public void setTcSpasId(String tcSpasId) {
		this.tcSpasId = tcSpasId;
	}
	public String getTcProjectName() {
		return tcProjectName;
	}
	public void setTcProjectName(String tcProjectName) {
		this.tcProjectName = tcProjectName;
	}
	public String getDmsSpasId() {
		return dmsSpasId;
	}
	public void setDmsSpasId(String dmsSpasId) {
		this.dmsSpasId = dmsSpasId;
	}
	public String getDmsProjectName() {
		return dmsProjectName;
	}
	public void setDmsProjectName(String dmsProjectName) {
		this.dmsProjectName = dmsProjectName;
	}
	public String getDmsFolderId() {
		return dmsFolderId;
	}
	public void setDmsFolderId(String dmsFolderId) {
		this.dmsFolderId = dmsFolderId;
	}
	
	@Override
	public String toString() {
		return "ProjectInfo [tcSpasId=" + tcSpasId + ", tcProjectName=" + tcProjectName + ", dmsSpasId=" + dmsSpasId
				+ ", dmsProjectName=" + dmsProjectName + ", dmsFolderId=" + dmsFolderId + "]";
	}
}
