package historicaldataimport.domain;

public class SeriesInfo {

	private String seriesId;//系列ID
	private String customerId;//客户ID
	private String seriesName;//系列名
	private int resultsCount;//查询结果的数量
	
	public String getSeriesId() {
		return seriesId;
	}
	public void setSeriesId(String seriesId) {
		this.seriesId = seriesId;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getSeriesName() {
		return seriesName;
	}
	public void setSeriesName(String seriesName) {
		this.seriesName = seriesName;
	}
	public int getResultsCount() {
		return resultsCount;
	}
	public void setResultsCount(int resultsCount) {
		this.resultsCount = resultsCount;
	}
	
	@Override
	public String toString() {
		return "SeriesInfo [seriesId=" + seriesId + ", customerId=" + customerId + ", seriesName=" + seriesName
				+ ", resultsCount=" + resultsCount + "]";
	}
	
}
