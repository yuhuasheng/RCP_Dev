import java.util.ArrayList;
import java.util.List;

public class DtL5BomChangeData<T> {

	private List<BomParentChild<T>> addData = new ArrayList<>();
	private List<BomParentChild<T>> delData = new ArrayList<>();
	
	public List<BomParentChild<T>> getAddData() {
		return addData;
	}
	public void setAddData(List<BomParentChild<T>> addData) {
		this.addData = addData;
	}
	public List<BomParentChild<T>> getDelData() {
		return delData;
	}
	public void setDelData(List<BomParentChild<T>> delData) {
		this.delData = delData;
	}
	
	@Override
	public String toString() {
		return "DtL5BomChangeData [addData=" + addData + ", delData=" + delData + "]";
	}
	
}
