package com.hh.tools.report.storage;

import com.teamcenter.rac.kernel.TCComponentItemRevision;

public class SingleEnvlistStorage {
    private String mfr;
    private String mfrPN;
    private String hhpn;
    private String itemRev = ""; // item_revision_id
    private String objectDesc;
    private String prjName;
    private String hfpn;
    private TCComponentItemRevision childRev;

    public String getMfr() {
        return mfr;
    }

    public void setMfr(String mfr) {
        this.mfr = mfr;
    }

    public String getMfrPN() {
        return mfrPN;
    }

    public void setMfrPN(String mfrPN) {
        this.mfrPN = mfrPN;
    }

    public String getHhpn() {
        return hhpn;
    }

    public void setHhpn(String hhpn) {
        this.hhpn = hhpn;
    }

    public String getObjectDesc() {
        return objectDesc;
    }

    public void setObjectDesc(String objectDesc) {
        this.objectDesc = objectDesc;
    }

    public String getPrjName() {
        return prjName;
    }

    public void setPrjName(String prjName) {
        this.prjName = prjName;
    }

    public String getHfpn() {
        return hfpn;
    }

    public void setHfpn(String hfpn) {
        this.hfpn = hfpn;
    }

    public TCComponentItemRevision getChildRev() {
        return childRev;
    }

    public void setChildRev(TCComponentItemRevision childRev) {
        this.childRev = childRev;
    }

    public String getItemRev() {
        return itemRev;
    }

    public void setItemRev(String itemRev) {
        this.itemRev = itemRev;
    }

}
