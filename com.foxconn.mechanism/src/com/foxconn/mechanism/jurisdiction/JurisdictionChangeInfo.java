package com.foxconn.mechanism.jurisdiction;

import com.teamcenter.rac.kernel.TCComponentBOMLine;

public class JurisdictionChangeInfo {

	private TCComponentBOMLine bomLine;
	private String owner;
	private String assignUser;
	private String personLiable;
	private String mailbox;
	
	public JurisdictionChangeInfo(){
		
	}
	
	public JurisdictionChangeInfo(TCComponentBOMLine bomLine, String owner, String assignUser, String personLiable,
			String mailbox) {
		super();
		this.bomLine = bomLine;
		this.owner = owner;
		this.assignUser = assignUser;
		this.personLiable = personLiable;
		this.mailbox = mailbox;
	}

	public TCComponentBOMLine getBomLine() {
		return bomLine;
	}

	public void setBomLine(TCComponentBOMLine bomLine) {
		this.bomLine = bomLine;
	}

	public String getOwner() {
		return owner;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}

	public String getAssignUser() {
		return assignUser;
	}

	public void setAssignUser(String assignUser) {
		this.assignUser = assignUser;
	}

	public String getPersonLiable() {
		return personLiable;
	}

	public void setPersonLiable(String personLiable) {
		this.personLiable = personLiable;
	}

	public String getMailbox() {
		return mailbox;
	}

	public void setMailbox(String mailbox) {
		this.mailbox = mailbox;
	}

	@Override
	public String toString() {
		return "JurisdictionChangeInfo [bomLine=" + bomLine + ", owner=" + owner + ", assignUser=" + assignUser
				+ ", personLiable=" + personLiable + ", mailbox=" + mailbox + "]";
	}

}
